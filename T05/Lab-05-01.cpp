#include <iostream>
#include <string>

// 1. Оголошення структури
struct BOOK {
    std::string author;   // Прізвище та ініціали автора
    int year;             // Рік видання
    int ratings[5];       // Масив оцінок всередині структури
    double averageRating; // Додаткове поле для зручності обчислень
};

int main() {
    // Для підтримки української мови в консолі (Windows)
    std::system("chcp 65001 > nul");

    const int SIZE = 3; // Для прикладу беремо 3 книги (у реальній задачі може бути більше)
    BOOK catalog[SIZE];

    // =========================================================
    // КРОК 1: Введення даних
    // =========================================================
    std::cout << "--- Введення даних до каталогу ---\n";
    for (int i = 0; i < SIZE; ++i) {
        std::cout << "\nКнига #" << i + 1 << ":\n";

        std::cout << "Прізвище та ініціали автора: ";
        // 🚨 ВАЖЛИВО: Оскільки ми використовуємо getline, текст може зчитатися порожнім,
        // якщо до цього в буфері залишився символ Enter (\n) від вводу чисел.
        // Тому перед кожним getline (крім найпершого) потрібно очищати буфер:
        if (i > 0) {
            std::cin.ignore();
        }
        std::getline(std::cin, catalog[i].author);

        std::cout << "Рік видання: ";
        std::cin >> catalog[i].year;

        std::cout << "Введіть 5 оцінок критиків через пробіл (від 1 до 10): ";
        double sum = 0;
        for (int j = 0; j < 5; ++j) {
            std::cin >> catalog[i].ratings[j]; // Звернення до масиву всередині структури
            sum += catalog[i].ratings[j];
        }

        // Одразу обчислюємо і зберігаємо середню оцінку
        catalog[i].averageRating = sum / 5.0;
    }

    // =========================================================
    // КРОК 2: Сортування масиву структур (Метод бульбашки)
    // =========================================================
    // Ми порівнюємо окреме поле (year), але міняємо місцями ЦІЛІ СТРУКТУРИ!
    for (int i = 0; i < SIZE - 1; ++i) {
        for (int j = 0; j < SIZE - i - 1; ++j) {
            // Сортування за зростанням року видання
            if (catalog[j].year > catalog[j + 1].year) {
                // Міняємо місцями всі дані книги через допоміжну змінну типу BOOK
                BOOK temp = catalog[j];
                catalog[j] = catalog[j + 1];
                catalog[j + 1] = temp;
            }
        }
    }

    // =========================================================
    // КРОК 3: Пошук та виведення результатів
    // =========================================================
    std::cout << "\n--- Книги з рейтингом вище 8.0 (Відсортовані за роком) ---\n";
    bool found = false; // Прапорець для перевірки, чи знайшли ми хоча б одну таку книгу

    for (int i = 0; i < SIZE; ++i) {
        if (catalog[i].averageRating > 8.0) {
            std::cout << catalog[i].year << " р. | Автор: "
                      << catalog[i].author << " | Рейтинг: "
                      << catalog[i].averageRating << std::endl;
            found = true;
        }
    }

    if (!found) {
        std::cout << "Книг із середнім рейтингом вище 8.0 не знайдено." << std::endl;
    }

    return 0;
}
